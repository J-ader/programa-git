# Inicializar listas para números pares e impares
pares = []
impares = []

# Recorrer los números del 0 al 100
for i in range(100):
    if i % 2 == 0:
        pares.append(i)
    else:
        impares.append(i)

# Mostrar resultados
print("Números pares:", pares)
print()
print("Números impares:", impares)




